package z017_conditional_operators;

public class ConditionalOperators {
    public static void main(String[] args) {
        int age = 21;

        System.out.println(age > 50 ? "You are old" : "You are young");

        /***
         *      ? is checking the equation before it.. here is age > 50?
         *      and then it will print either of the statement
         *      true : false
         *      true statement is print if condition is true
         *      otherwise
         *      false statement will print...
         */
    }
}
