package z022_math_class_methods;

public class MathClassMethods {
    public static void main(String[] args) {
        System.out.println(Math.abs(-26.7));  // absolute value
        System.out.println(Math.ceil(7.4));     // ceil value to up the number round figure..
        System.out.println(Math.floor(7.8));    // floor make down the number round figure..
        System.out.println(Math.max(8.6, 5.2)); // maximum of two numbers..
        System.out.println(Math.min(8.6, 5.2)); // minimum of two numbers..
        System.out.println(Math.pow(5, 3));     // power.. here 5 to power of 3
        System.out.println(Math.sqrt(9));       // square root the value
    }
}
